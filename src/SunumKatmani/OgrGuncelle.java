    int secilendeger;
    private void btnOgrSecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOgrSecActionPerformed
        // TODO add your handling code here:
        int tablosatir= jTable1.getSelectedRow();//Hangi satır olduğunu alıyor.
        secilendeger=(int) jTable1.getValueAt(tablosatir,0);
        OgrencilerFunc getir = new OgrencilerFunc();
        Uye gelen = getir.ogrenciGetir(secilendeger);
        txtisim.setText(gelen.isim);
        txtalan.setText(gelen.alan);
    }//GEN-LAST:event_btnOgrSecActionPerformed

    private void btnGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuncelleActionPerformed
        // TODO add your handling code here:
        Uye ogrGuncellenecek = new Uye();
        ogrGuncellenecek.Id = secilendeger;
        ogrGuncellenecek.isim = txtisim.getText();
        ogrGuncellenecek.bolum = (String)cmbsinif.getSelectedItem();
        ogrGuncellenecek.alan = txtalan.getText();
        ogrGuncellenecek.yil = (String)cmbyil.getSelectedItem();
        ogrGuncellenecek.donem = (String)cmbdonem.getSelectedItem();
        OgrencilerFunc ogrenci = new OgrencilerFunc();
        ogrenci.guncelle(ogrGuncellenecek);
        jTable1.setModel(DbUtils.resultSetToTableModel(yeni.listele()));
    }//GEN-LAST:event_btnGuncelleActionPerformed

